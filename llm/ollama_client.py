from __future__ import annotations

from typing import Iterable

import requests

from models.leak_result import LeakResult


class OllamaClient:

    API_URL = "http://localhost:11434/api/generate"
    MODEL = "llama3.2"

    def generate_recommendations(self, leaks: Iterable[LeakResult]) -> str:
        leaks_list = list(leaks)
        if not leaks_list:
            return "Утечки не обнаружены. Рекомендации не требуются."

        prompt_lines = [
            "Ты ассистент по безопасности программного обеспечения.",
            "Твоя задача — анализировать возможные утечки секретов в исходном коде.",
            "",
            "Важные правила:",
            "1. Анализируй ТОЛЬКО те утечки, которые перечислены в списке 'Утечки для анализа'.",
            "2. Количество утечек в ответе должно СТРОГО соответствовать количеству утечек во входных данных.",
            "3. НИ В КОЕМ СЛУЧАЕ НЕ ДОБАВЛЯЙ утечки, которых нет в списке.",
            "4. НЕ ВЫДУМЫВАЙ утечки на соседних строках или из контекста.",
            "5. Если во входных данных 2 утечки - в ответе должно быть ровно 2 утечки.",
            "6. Каждую утечку анализируй отдельно.",
            "7. Для каждой утечки укажи:",
            "   - краткое описание проблемы",
            "   - уровень риска",
            "   - практическое решение",
            "8. Не объясняй теорию.",
            "9. Ответы должны быть краткими и практичными.",
            "10. Отвечай на русском языке.",
            "11. Только деловая информация. Без вступлений, без похвал.",
            "12. Не используй маркдаун, только текст.",
            "13. ИСПОЛЬЗУЙ ТОЛЬКО РУССКИЕ СЛОВА. Не применяй английские слова там, где есть русские аналоги.",
            "",
            "СЛОВАРЬ ТИПОВ УТЕЧЕК (используй ТОЧНО эти соответствия):",
            "",
            "High entropy string -> Высокоэнтропийная строка",
            "Password -> Пароль",
            "AWS Access Key -> Ключ доступа AWS",
            "GitHub Token -> Токен GitHub",
            "RSA Private Key -> Закрытый ключ RSA",
            "JWT Secret -> Секретный ключ JWT",
            "API Key -> Ключ API",
            "Database Credentials -> Учетные данные БД",
            "OAuth Token -> Токен OAuth",
            "SSH Private Key -> Закрытый ключ SSH",
            "Encryption Key -> Ключ шифрования",
            "Bearer Token -> Токен авторизации",
            "Connection String -> Строка подключения",
            "Secret Key -> Секретный ключ",
            "Login Credentials -> Учетные данные",
            "Token -> Токен",
            "Credential -> Учетные данные",
            "Secret -> Секретные данные",
            "Key -> Ключ",
            "",
            "ВАЖНО: Для типа утечки используй ТОЛЬКО термины из словаря выше.",
            "НЕ перефразируй: 'Конфиденциальные данные', 'Учетные записи' и т.п.",
            "Если тип утечки не найден в словаре, используй 'Секретные данные'.",
            "",
            "Формат вывода (строго соблюдай этот стиль):",
            "",
            "Утечка:",
            "Тип: <тип из словаря выше>",
            "Местоположение: <имя_файла:строка>",
            "Риск: <краткое описание риска одной фразой на чистом русском>",
            "Исправление: <действия по шагам в формате '1) ... 2) ...'>",
            "",
            "Пример правильного ответа:",
            "",
            "Утечка:",
            "Тип: Высокоэнтропийная строка",
            "Местоположение: docker-compose-local.yml:12",
            "Риск: Посторонние лица могут получить доступ к защищаемой информации",
            "Исправление:",
            "",
            "1) Удалите конфиденциальные данные из файла docker-compose-local.yml.",
            "2) Добавьте шифрование для защищаемой информации.",
            "3) Используйте переменные окружения для хранения секретных данных.",
            "",
            "ВАЖНО:",
            "- КОЛИЧЕСТВО утечек в ответе должно быть РОВНО СТОЛЬКО ЖЕ, сколько в списке ниже",
            "- НЕ ДОБАВЛЯЙ утечки, которых нет в списке",
            "- НЕ ВЫДУМЫВАЙ утечки на соседних строках",
            "- Используй ТОЧНО такой же формат как в примере",
            "- После 'Исправление:' используй цифры с скобками: 1) ... 2) ...",
            "- НЕ ИСПОЛЬЗУЙ английские слова: 'сеcrets', 'strings', 'login', 'token'",
            "- ТИП УТЕЧКИ бери ТОЛЬКО из словаря выше",
            "- ОДИН И ТОТ ЖЕ ТИП утечки должен называться ОДИНАКОВО во всех случаях",
            "",
            "Теперь проанализируй следующие утечки (всего {len(leaks_list)} утечек):",
            "",
        ]

        for leak in leaks_list[:50]:
            prompt_lines.append(
                f"- {leak.secret_type} | {leak.risk_level} | {leak.file_path}:{leak.line_number} | Фрагмент: {leak.code_fragment}"
            )

        payload = {
            "model": self.MODEL,
            "prompt": "\n".join(prompt_lines),
            "stream": False,
        }

        try:
            response = requests.post(self.API_URL, json=payload, timeout=30)
            response.raise_for_status()
            data = response.json()
            return str(data.get("response", "Не удалось получить ответ от модели.")).strip()
        except requests.RequestException as exc:
            return (
                "Не удалось получить рекомендации от Ollama. "
                f"Проверьте запуск сервера на {self.API_URL}. Ошибка: {exc}"
            )